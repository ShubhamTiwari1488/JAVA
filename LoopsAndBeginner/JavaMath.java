
/*_________________USING SQRT TO FIND HYPOTENEUS OF THE TRIANGLE__________________________*/

class Hypoteneus{
  public static void main(String args[]){
  
  double a=3.0 ,b=4.0;
  
  double c=Math.sqrt(a*a+b*b);
  
  System.out.print("\nThe Hypoteneus of the triangle with sides 3 and 4 is : ");
  System.out.println(c+"\n");
  
 }
}
  
