
/*_________________DEMONSTRATING LOOP IN JAVA___________________*/

class Loop{
 public static void main(String args[]){
 
 int i;
 
 System.out.println("Printing numbers in a loop from one to 10\n");
 
 for(i=0;i<10;i++)
 {
  System.out.println(i+1);
 }
 
 System.out.println("\nEnd of the loop\n");
 }
}
  
