
/*________DEMONSTRATING BOOLEAN LOGICAL OPERATOR_____________________*/

class BooleanLogicOperator{
   public static void main(String args[]){
   
   boolean a=true;
   boolean b=false;
   
   boolean c=a&b;      
   boolean d=a|b;
   boolean e=a^b;
   boolean f=!a;
   
   System.out.println("Logical AND Operator "+c);
   System.out.println("Logical OR Operator "+d);
   System.out.println("Logical XOR Operator "+e);
   System.out.println("Logical NOT Operator 'NOT a' "+f);
   
  }

}
   
